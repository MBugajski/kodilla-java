
public class Wrappers {
    public static void main(String[] args) {
        boolean b1 = true;
        Boolean b2 = true;
        int i1 = 1;
        Integer i2 = 1;
        char c1 = 'a';
        Character c2 = 'a';

        System.out.println(b1 == b2);
        System.out.println(i1 == i2);
        System.out.println(c1 == c2);
    }
}
